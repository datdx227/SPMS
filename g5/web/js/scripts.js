window.addEventListener('DOMContentLoaded', event => {
    const currentUrl = window.location.href;
    const anchor = document.getElementById('logo');
    if (/dashboard/.test(currentUrl) || currentUrl === 'http://localhost:8080/SPM/settinglist' || currentUrl === 'http://localhost:8080/SPM//settingdetails')
    {
        anchor.insertAdjacentHTML('afterend', '<button class="btn btn-link btn-sm order-1 order-lg-0 me-4 me-lg-0" id="sidebarToggle" href="#!"><i class="fas fa-bars"></i></button>');
    } else {
        anchor.parentNode.removeChild(document.getElementById('sidebarToggle'));
    }
    // Toggle the side navigation
    const sidebarToggle = document.body.querySelector('#sidebarToggle');
    if (sidebarToggle) {
        // Uncomment Below to persist sidebar toggle between refreshes
        // if (localStorage.getItem('sb|sidebar-toggle') === 'true') {
        //     document.body.classList.toggle('sb-sidenav-toggled');
        // }
        sidebarToggle.addEventListener('click', event => {
            event.preventDefault();
            document.body.classList.toggle('sb-sidenav-toggled');
            localStorage.setItem('sb|sidebar-toggle', document.body.classList.contains('sb-sidenav-toggled'));
        });
    }

});
